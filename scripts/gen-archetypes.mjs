function emit(name, arr) {
  const lines = [
    `import { archetype } from "./types";`,
    `import type { ArchetypeOption } from "./types";`,
    ``,
    `export const ${name}: ArchetypeOption[] = [`,
  ];
  for (const item of arr) {
    lines.push("  archetype({");
    lines.push(`    name: ${JSON.stringify(item.name)},`);
    lines.push(`    tagline: ${JSON.stringify(item.tagline)},`);
    lines.push(`    keywords: ${JSON.stringify(item.keywords)},`);
    lines.push(`    imageUrl: ${JSON.stringify(item.imageUrl)},`);
    lines.push(`    shortPoem: ${JSON.stringify(item.shortPoem)},`);
    lines.push(`    deepAnalysis: ${JSON.stringify(item.deepAnalysis)},`);
    lines.push(`    resonanceStory: ${JSON.stringify(item.resonanceStory)},`);
    lines.push(`    growthInsight: ${JSON.stringify(item.growthInsight)},`);
    lines.push("  }),");
  }
  lines.push("];");
  lines.push("");
  const file =
    name === "PLANT_ARCHETYPES"
      ? "plants.ts"
      : name === "ANIMAL_ARCHETYPES"
        ? "animals.ts"
        : `${name}.ts`;
  fs.writeFileSync(path.join(root, "lib/archetypes", file), lines.join("\n"));
}

emit("PLANT_ARCHETYPES", plants);
emit("ANIMAL_ARCHETYPES", animals);
